package Operators;

public class Compound_Assignment {

    public static void main(String[] args) {

        int a=10;
        int b=20;

         a -= 45;  // output is -35 (a= a - 45)
         b += 30;  // output is 50  (b= b + 30)

        System.out.println(a);
        System.out.println(b);

    }

}
