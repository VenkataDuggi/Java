package OOPS.Abstraction.Interface;

public class Pig implements Animal{


    public void sleep() {

        System.out.println("Pig will sleep 12 hours");
    }


    public void sound() {

        System.out.println("Pig will sound horribly");
    }


    public void eat() {

        System.out.println("Pig will eat 3 times");
    }
}
